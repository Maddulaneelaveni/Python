# Basic string operations:

# a) Concatenation (joining)
first = "Gram"
second = "Ujjwala"
result = first + " " + second   # "Gram Ujjwala"


# b) Repetition
s = "Ha"
print(s * 3)   # "HaHaHa"

# c) Accessing characters
word = "Python"
first_char = word[0]   # 'P'
last_char = word[-1]   # 'n'
print("First character:", first_char)
print("Last character:", last_char)

