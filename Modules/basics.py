# What is module ?
# A module is simply a Python file that contains code which can be reused in other Python programs.
# So, simply a Module is a python file containing reusable code.

# Modules can contain functions, classes, and variables that can be imported and used in other Python files.
# Modules are a way to organize and reuse code, making it easier to maintain and share across different projects.

# To use a module, you can import it using the import statement. For example, if you have a module named my_module.py, you can import it in another Python file like this:
# import my_module
# After importing the module, you can access its functions, classes, and variables using the module name as a prefix. For example, if my_module.py contains a function called my_function(), you can call it like this:
# my_module.my_function()

# A module file always ends with the .py extension. You can create your own modules by simply writing Python code in a file and saving it with a .py extension. You can then import and use that module in other Python files as needed.
# A module file always ends with: .py

# Example module files:
# math.py
# random.py
# datetime.py
# my_module.py
# calculator.py

# Benefits of modules :

# 1. Code Reusability: Modules allow you to reuse code across multiple programs, reducing redundancy and improving efficiency.
# 2. Organization: Modules help organize code into logical sections, making it easier to read and maintain.
# 3. Namespace Management: Modules provide a separate namespace for their contents, preventing naming conflicts between different parts of a program.
# 4. Easier debugging: By breaking code into modules, it becomes easier to identify and fix bugs, as you can isolate specific functionality within a module.