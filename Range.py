# What is range() in Python?
# range() is a built-in function used to generate a sequence of integers.
# It is most commonly used in loops, especially for loops.
# Important: range() does not create a list.
# It returns a range object (lazy evaluation â†’ memory efficient).

# Syntax of range():
# range(start, stop, step)
# start: Starting number (inclusive, default is 0)
# stop: Stopping number (exclusive)
# step: Difference between each number (default is 1)

# Examples of range():
for i in range(1, 10, 2):
    print(i)
# output:
# 1 3 5 7 9



