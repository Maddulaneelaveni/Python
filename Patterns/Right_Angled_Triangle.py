# Right angled triangle pattern

n = 5  # no.of rows
for i in range(1, n+1):  # loop from 1 to 5
    print("*" * i)   # print i stars in each row

# output:

#*
#**
#***
#****
#*****



# Inverted right angled triangle

n = 5   # no.of rows
for i in range(n, 0, -1):  # loop from 5 to 1
    print("*" * i)  # print i stars in each rrow

# output:

#*****
#****
#***
#**
#*

